package com.blazebit.persistence.spring.data.testsuite.webmvc.entity;

import java.io.Serializable;
import java.util.Objects;

/**
 * @author Christian Beikov
 * @since 1.6.8
 */
public class IdClassEntityId implements Serializable {
    private static final long serialVersionUID = 1L;

    private String id;
    private String name;

    public IdClassEntityId() {
    }

    public IdClassEntityId(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        IdClassEntityId that = (IdClassEntityId) o;

        if (!Objects.equals(id, that.id)) {
            return false;
        }
        return Objects.equals(name, that.name);
    }

    @Override
    public int hashCode() {
        int result = id != null ? id.hashCode() : 0;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        return result;
    }
}
